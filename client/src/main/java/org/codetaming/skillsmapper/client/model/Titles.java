package org.codetaming.skillsmapper.client.model;

import java.util.List;

public class Titles {

    List<Title> titles;

    public List<Title> getTags() {
        return titles;
    }

    public List<Title> getTitles() {
        return titles;
    }

    @Override
    public String toString() {
        return "Titles{" +
                "titles=" + titles +
                '}';
    }

}
